CREATE OR REPLACE VIEW uvpan
    -- 칼럼명 그대로 사용
AS 
SELECT b.b_id, title, price, g.g_id, g_name, p_date, p_su* price amt
FROM book b JOIN danga d ON b.b_id = d.b_id
            JOIN panmai p ON b.b_id = p.b_id
            JOIN gogaek g ON p.g_id = g.g_id;
            
            select *
from uvpan -- 뷰
where b_id = 'a-1'
ORDER by p_date; 

CREATE OR REPLACE VIEW uvgogaek
AS
select to_char( p_date, 'YYYY' )p_year
        , to_char( p_date, 'MM')p_month
        , g.g_id, g_name,Sum( price*p_su) 판매금액합
FROm panmai p JOIN gogaek g on p.g_id = g.g_id
              JOIN danga d on p.b_id = d.b_id
GROUP by to_char( p_date, 'YYYY' )
        , to_char( p_date, 'MM')
        , g.g_id, g_name
ORDER BY p_year, p_month;